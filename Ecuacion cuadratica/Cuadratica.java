
public class Cuadratica {

    private double A, B, C;

    public void setA(double a) {
        this.A = a;
    }

    public void setB(double b) {
        this.B = b;
    }

    public void setC(double c) {
        this.C = c;
    }

    public double geta() {
        return A;
    }

    public double getb() {
        return B;
    }

    public double getc() {
        return C;
    }
}
