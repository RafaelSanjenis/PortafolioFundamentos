
public class Variables {

    private int U, D, T;

    public void setU(int u) {
        this.U = u;
    }

    public void setD(int d) {
        this.D = d;
    }

    public void setT(int t) {
        this.T = t;
    }

    public int getu() {
        return U;
    }

    public int getd() {
        return D;
    }

    public int gett() {
        return T;
    }
}
